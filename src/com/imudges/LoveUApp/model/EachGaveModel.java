public class EachGaveModel {
    private String msg;
    private Integer state;
    private String UserName;
    private String GaveUser;
    private String GiveInformation;
    private String Giveimage;
    private String GetUser;

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public Integer getState() {
        return state;
    }

    public void setState(Integer state) {
        this.state = state;
    }

    public String getUserName() {
        return UserName;
    }

    public void setUserName(String userName) {
        UserName = userName;
    }

    public String getGaveUser() {
        return GaveUser;
    }

    public void setGaveUser(String gaveUser) {
        GaveUser = gaveUser;
    }

    public String getGiveInformation() {
        return GiveInformation;
    }

    public void setGiveInformation(String giveInformation) {
        GiveInformation = giveInformation;
    }

    public String getGiveimage() {
        return Giveimage;
    }

    public void setGiveimage(String giveimage) {
        Giveimage = giveimage;
    }

    public String getGetUser() {
        return GetUser;
    }

    public void setGetUser(String getUser) {
        GetUser = getUser;
    }
}
